DELIMITER $$
CREATE FUNCTION SQUARE
(
 Num INT
)
RETURNS INT 
DETERMINISTIC
BEGIN
    DECLARE SQUARE INT;
    SET SQUARE = Num * Num ;
    RETURN SQUARE; 
END$$
DELIMITER ;

